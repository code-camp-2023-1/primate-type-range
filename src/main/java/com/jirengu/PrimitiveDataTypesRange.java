package com.jirengu;

public class PrimitiveDataTypesRange {
    // byte类型能表示的最大值/最小值
    public static byte MAX_BYTE = 1;
    public static byte MIN_BYTE = 1;

    // short类型能表示的最大值/最小值
    public static short MAX_SHORT = 1;
    public static short MIN_SHORT = 1;
    // int类型能表示的最大值/最小值
    public static int MAX_INT = 1;
    public static int MIN_INT = 1;
    // long类型能表示的最大值/最小值
    public static long MAX_LONG = 1;
    public static long MIN_LONG = 1;

    // float类型能表示的最大值/最小值
    public static float MAX_FLOAT = 1.0f;
    public static float MIN_FLOAT = 1.0f;

    // double类型能表示的最大值/最小值
    public static double MAX_DOUBLE = 1.0;
    public static double MIN_DOUBLE = 1.0;

    // char类型能表示的最大值/最小值？
    public static char MAX_CHAR = 1;
    public static char MIN_CHAR = 1;

    public static void main(String[] args) {
        System.out.println("Max byte: "+ MAX_BYTE);
        System.out.println("Min byte: "+ MIN_BYTE);
        System.out.println("Max short: "+ MAX_SHORT);
        System.out.println("Min short: "+ MIN_SHORT);
        System.out.println("Max int: "+ MAX_INT);
        System.out.println("Min int: "+ MIN_INT);
        System.out.println("Max long: "+ MAX_LONG);
        System.out.println("Min long: "+ MIN_LONG);
        System.out.println("Max float: "+ MAX_FLOAT);
        System.out.println("Min float: "+ MIN_FLOAT);
        System.out.println("Max double: "+ MAX_DOUBLE);
        System.out.println("Min double: "+ MIN_DOUBLE);
        System.out.println("Max char: "+ MAX_CHAR);
        System.out.println("Min char: "+ MIN_CHAR);
    }
}
